    function bindExamEvents() {
        var app = document.getElementById('mock-exam-app');
        if (!app) return;

        var backBtn = document.getElementById('mock-exam-back');
        if (backBtn) {
            backBtn.addEventListener('click', function() {
                if (state.submitted) {
                    renderSubjectSelect();
                    return;
                }
                if (confirm('确定要退出考试吗？当前作答将不会保存。')) {
                    clearTimer();
                    renderSubjectSelect();
                }
            });
        }

        var submitBtn = document.getElementById('mock-exam-submit');
        if (submitBtn) {
            submitBtn.addEventListener('click', function() {
                if (state.submitted) return;
                collectAnswers();
                var answered = 0;
                for (var k in state.userAnswers) {
                    if (state.userAnswers.hasOwnProperty(k) && state.userAnswers[k]) answered++;
                }
                var total = state.questions.length;
                var msg = '确定交卷吗？';
                if (answered < total) {
                    msg += '\n\n⚠ 还有 ' + (total - answered) + ' 道题未作答！';
                }
                if (confirm(msg)) {
                    state.submitted = true;
                    clearTimer();
                    renderResults();
                }
            });
        }

        var optionInputs = app.querySelectorAll('.mock-exam-option-input');
        for (var i = 0; i < optionInputs.length; i++) {
            optionInputs[i].addEventListener('change', function() {
                collectAnswers();
                updateProgressBar();
            });
        }

        var blankInputs = app.querySelectorAll('.mock-exam-blank-input');
        for (var j = 0; j < blankInputs.length; j++) {
            blankInputs[j].addEventListener('input', function() {
                collectAnswers();
                updateProgressBar();
            });
        }

        var essayInputs = app.querySelectorAll('.mock-exam-essay-input');
        for (var k = 0; k < essayInputs.length; k++) {
            essayInputs[k].addEventListener('input', function() {
                collectAnswers();
                updateProgressBar();
            });
        }
    }

    function collectAnswers() {
        var app = document.getElementById('mock-exam-app');
        if (!app) return;

        for (var i = 0; i < state.questions.length; i++) {
            var q = state.questions[i];

            if (q.isEssay) {
                var essayEl = document.getElementById('mock-exam-essay-' + i);
                state.userAnswers[i] = essayEl ? essayEl.value : '';
            } else if (q.options && q.options.length > 0) {
                var isMulti = (q.type === '多选');
                if (isMulti) {
                    var checked = app.querySelectorAll('input[name="mock-exam-q' + i + '"]:checked');
                    var letters = [];
                    for (var c = 0; c < checked.length; c++) {
                        letters.push(checked[c].value);
                    }
                    state.userAnswers[i] = letters.sort().join('');
                } else {
                    var selected = app.querySelector('input[name="mock-exam-q' + i + '"]:checked');
                    state.userAnswers[i] = selected ? selected.value : '';
                }
            } else {
                var blankEl = document.getElementById('mock-exam-blank-' + i);
                state.userAnswers[i] = blankEl ? blankEl.value.trim() : '';
            }
        }
    }

    function updateProgressBar() {
        collectAnswers();
        var answered = 0;
        var total = state.questions.length;
        for (var i = 0; i < total; i++) {
            var ans = state.userAnswers[i];
            if (ans && ans.length > 0) answered++;
        }
        var pct = Math.round((answered / total) * 100);
        var bar = document.getElementById('mock-exam-progress-bar');
        if (bar) {
            bar.style.width = pct + '%';
            bar.textContent = answered + '/' + total;
        }
    }

