    function renderSubjectAnalysis(scoreData) {
        var catMap = {};
        for (var i = 0; i < scoreData.results.length; i++) {
            var r = scoreData.results[i];
            var cat = r.category || '其他';
            if (!catMap[cat]) catMap[cat] = { total: 0, earned: 0, count: 0, correct: 0 };
            catMap[cat].total += r.points;
            catMap[cat].count++;
            if (r.isCorrect === true) {
                catMap[cat].correct++;
                catMap[cat].earned += r.points;
            } else if (r.isCorrect === null) {
                catMap[cat].earned += Math.round(r.points * 0.7);
            }
        }

        var html = '<table class="mock-exam-analysis-table">';
        html += '<thead><tr><th>知识板块</th><th>题数</th><th>得分</th><th>总分</th><th>得分率</th></tr></thead><tbody>';
        var cats = [];
        for (var k in catMap) {
            if (catMap.hasOwnProperty(k)) cats.push({ name: k, data: catMap[k] });
        }
        cats.sort(function(a, b) { return b.data.total - a.data.total; });

        for (var j = 0; j < cats.length; j++) {
            var c = cats[j];
            var catPct = c.data.total > 0 ? Math.round((c.data.earned / c.data.total) * 100) : 0;
            var color = catPct >= 80 ? '#10b981' : (catPct >= 60 ? '#f59e0b' : '#ef4444');
            html += '<tr>';
            html += '<td>' + escHtml(c.name) + '</td>';
            html += '<td>' + c.data.count + '</td>';
            html += '<td>' + c.data.earned + '</td>';
            html += '<td>' + c.data.total + '</td>';
            html += '<td><span style="color:' + color + ';font-weight:700;">' + catPct + '%</span></td>';
            html += '</tr>';
        }
        html += '</tbody></table>';
        return html;
    }

