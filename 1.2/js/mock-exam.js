    function getQuestionPool(subject) {
        var pool = [];

        try {
            if (window.examBank && window.examBank[subject] && window.examBank[subject].exams) {
                var bankQuestions = window.examBank[subject].exams;
                for (var i = 0; i < bankQuestions.length; i++) {
                    var q = bankQuestions[i];
                    pool.push({
                        id: q.id || ('BANK_' + i),
                        type: q.type || '单选',
                        points: q.points || 4,
                        category: q.category || '',
                        question: q.question || '',
                        options: q.options || [],
                        answer: q.answer || '',
                        analysis: q.analysis || ''
                    });
                }
            }
        } catch (e) {
            // Silently fall back to embedded
        }

        if (pool.length < 10) {
            var embedded = EMBEDDED_QUESTIONS[subject] || [];
            for (var j = 0; j < embedded.length; j++) {
                var dup = false;
                for (var k = 0; k < pool.length; k++) {
                    if (pool[k].id === embedded[j].id) { dup = true; break; }
                }
                if (!dup) {
                    pool.push(embedded[j]);
                }
            }
        }

        return pool;
    }

    function groupByType(pool) {
        var groups = {};
        for (var i = 0; i < pool.length; i++) {
            var t = pool[i].type;
            if (!groups[t]) groups[t] = [];
            groups[t].push(pool[i]);
        }
        return groups;
    }

    function buildExam(subject) {
        var config = EXAM_CONFIG[subject];
        var pool = getQuestionPool(subject);
        var groups = groupByType(pool);
        var selected = [];
        var questionNum = 1;

        for (var s = 0; s < config.structure.length; s++) {
            var slot = config.structure[s];
            var type = slot.type;
            var count = slot.count;
            var pointsEach = slot.pointsEach;

            var lookupType = type;
            if (subject === 'math' && type === '解答2') {
                lookupType = '解答';
            }

            var candidates = groups[lookupType] || [];

            if (candidates.length < count) {
                // Broaden: use all remaining questions in pool
                var usedIds = {};
                for (var u = 0; u < selected.length; u++) { usedIds[selected[u].id] = true; }
                candidates = [];
                for (var p = 0; p < pool.length; p++) {
                    if (!usedIds[pool[p].id]) candidates.push(pool[p]);
                }
                candidates = shuffle(candidates);
            } else {
                candidates = shuffle(candidates);
            }

            for (var c = 0; c < count && c < candidates.length; c++) {
                var q = candidates[c];
                selected.push({
                    index: selected.length,
                    num: questionNum++,
                    id: q.id,
                    type: type,
                    points: pointsEach,
                    category: q.category || '',
                    question: q.question,
                    options: q.options,
                    answer: (q.answer || '').trim().toUpperCase(),
                    analysis: q.analysis || '',
                    isEssay: (type === '作文')
                });

                // Remove from groups
                if (groups[lookupType]) {
                    for (var r2 = 0; r2 < groups[lookupType].length; r2++) {
                        if (groups[lookupType][r2].id === q.id) {
                            groups[lookupType].splice(r2, 1);
                            break;
                        }
                    }
                }
            }
        }

        return selected;
    }

