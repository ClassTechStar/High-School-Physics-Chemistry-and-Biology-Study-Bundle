    function bindGlobalDelegates() {
        var app = document.getElementById('mock-exam-app');
        if (!app) return;

        app.addEventListener('click', function(e) {
            var target = e.target;

            if (target.id === 'mock-exam-clear-history' || (target.closest && target.closest('#mock-exam-clear-history'))) {
                if (confirm('确定要清空所有历史考试记录吗？此操作不可撤销。')) {
                    clearHistory();
                    renderSubjectSelect();
                }
            }

            if (target.classList.contains('mock-exam-review-btn') || (target.closest && target.closest('.mock-exam-review-btn'))) {
                var btn = target.classList.contains('mock-exam-review-btn') ? target : target.closest('.mock-exam-review-btn');
                var idx = parseInt(btn.getAttribute('data-history-idx'), 10);
                if (!isNaN(idx)) {
                    viewHistoryEntry(idx);
                }
            }
        });
    }

    function viewHistoryEntry(idx) {
        var history = getHistory();
        if (idx < 0 || idx >= history.length) return;
        var entry = history[idx];
        var config = EXAM_CONFIG[entry.subject];
        var subName = config ? config.name : entry.subject;
        alert('【历史考试记录】\n\n科目：' + subName +
              '\n得分：' + entry.score + '/' + entry.totalPoints +
              '\n得分率：' + entry.percentage + '%' +
              '\n用时：' + formatTime(entry.timeUsed) +
              '\n答对：' + entry.correct + '/' + entry.total + '题' +
              '\n日期：' + entry.date);
    }

