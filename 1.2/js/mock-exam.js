    function calculateScore() {
        var correct = 0;
        var totalPoints = 0;
        var earnedPoints = 0;
        var results = [];

        for (var i = 0; i < state.questions.length; i++) {
            var q = state.questions[i];
            var userAns = (state.userAnswers[i] || '').trim().toUpperCase();
            var correctAns = (q.answer || '').trim().toUpperCase();

            totalPoints += q.points;

            var isCorrect = false;
            if (q.isEssay) {
                isCorrect = null;
                if (userAns.length > 20) {
                    earnedPoints += Math.round(q.points * 0.7);
                }
            } else if (q.type === '多选') {
                isCorrect = (userAns === correctAns);
                if (isCorrect) earnedPoints += q.points;
            } else if (q.type === '解答') {
                isCorrect = (userAns === correctAns);
                if (isCorrect) {
                    earnedPoints += q.points;
                } else if (userAns.length > 0) {
                    earnedPoints += Math.round(q.points * 0.3);
                }
            } else {
                isCorrect = (userAns === correctAns);
                if (isCorrect) {
                    earnedPoints += q.points;
                    correct++;
                }
            }

            results.push({
                index: i,
                num: q.num,
                type: q.type,
                points: q.points,
                question: q.question,
                userAnswer: userAns || '(未作答)',
                correctAnswer: correctAns,
                isCorrect: isCorrect,
                analysis: q.analysis,
                category: q.category,
                isEssay: q.isEssay
            });
        }

        return {
            totalQuestions: state.questions.length,
            correctCount: correct,
            totalPoints: totalPoints,
            earnedPoints: earnedPoints,
            percentage: totalPoints > 0 ? Math.round((earnedPoints / totalPoints) * 10000) / 100 : 0,
            results: results
        };
    }

