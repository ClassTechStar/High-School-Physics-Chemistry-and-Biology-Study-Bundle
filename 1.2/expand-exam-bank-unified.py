def parse_args():
    parser = argparse.ArgumentParser(
        description='广东省高考真题库统一扩充脚本（合并4个原始脚本）',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__
    )
    parser.add_argument('--batch', choices=['1', '2', '3', '4', 'all'], default='all',
                        help='选择执行的批次：1=手工精选真题, 2=基础练习, 3=综合应用, 4=实验/反应原理, all=全部 (默认: all)')
    parser.add_argument('--dry-run', action='store_true',
                        help='预演模式：只统计会添加的题目数量，不修改 exam-bank.json')
    parser.add_argument('--idempotent', action='store_true',
                        help='幂等模式：检查题目ID是否已存在，跳过重复题目')
    return parser.parse_args()


def main():
    args = parse_args()
    batches = [1, 2, 3, 4] if args.batch == 'all' else [int(args.batch)]

    print("=" * 60)
    print("广东省高考真题库统一扩充脚本")
    print(f"  执行批次: {', '.join(str(b) for b in batches)}")
    print(f"  预演模式: {'是 (--dry-run)' if args.dry_run else '否'}")
    print(f"  幂等模式: {'是 (--idempotent)' if args.idempotent else '否'}")
    print(f"  数据文件: {DATA_FILE}")
    print("=" * 60)

    bank = load_bank()
    existing_ids = get_existing_ids(bank)
    print(f"现有题库：物理 {bank['physics']['total']} 题，"
          f"化学 {bank['chemistry']['total']} 题，"
          f"生物 {bank['biology']['total']} 题，"
          f"总计 {bank['meta'].get('totalQuestions', 0)} 题")

    total_added = {'physics': 0, 'chemistry': 0, 'biology': 0}
    total_skipped = 0

    for b in batches:
        to_add, skipped = run_batch(bank, b, existing_ids, args.idempotent)
        total_skipped += skipped
        for sub in SUBJECTS:
            total_added[sub] += len(to_add[sub])
            if not args.dry_run:
                bank[sub]['exams'].extend(to_add[sub])
        added_sum = sum(len(to_add[s]) for s in SUBJECTS)
        skip_info = f"，跳过重复 {skipped} 题" if (args.idempotent and skipped) else ""
        print(f"批次{b}：新增 {added_sum} 题{skip_info}"
              f"（物理 {len(to_add['physics'])}，化学 {len(to_add['chemistry'])}，生物 {len(to_add['biology'])}）")

    if not args.dry_run:
        update_meta(bank)
        save_bank(bank)

    # 输出最终统计
    print("-" * 60)
    if args.dry_run:
        grand = sum(total_added.values())
        skip_total = f"，因幂等跳过 {total_skipped} 题" if (args.idempotent and total_skipped) else ""
        print(f"[预演] 本次将新增 {grand} 题{skip_total}（未写入文件）")
        print(f"  物理 +{total_added['physics']}，化学 +{total_added['chemistry']}，生物 +{total_added['biology']}")
    else:
        update_meta(bank)
        total = bank['meta']['totalQuestions']
        print(f"✅ 扩充完成！")
        print(f"   物理题数: {bank['physics']['total']} (新增{total_added['physics']})")
        print(f"   化学题数: {bank['chemistry']['total']} (新增{total_added['chemistry']})")
        print(f"   生物题数: {bank['biology']['total']} (新增{total_added['biology']})")
        print(f"   总题数: {total}")
        print(f"   覆盖年份: {bank['meta']['years']}")


if __name__ == '__main__':
    main()
