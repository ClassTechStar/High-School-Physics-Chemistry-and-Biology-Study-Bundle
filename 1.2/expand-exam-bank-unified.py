# =============================================================================
# 通用工具函数
# =============================================================================

def load_bank():
    """读取现有题库 JSON 文件。"""
    with open(DATA_FILE, 'r', encoding='utf-8') as f:
        return json.load(f)


def save_bank(bank):
    """将题库写回 JSON 文件。"""
    with open(DATA_FILE, 'w', encoding='utf-8') as f:
        json.dump(bank, f, ensure_ascii=False, indent=2)


def get_existing_ids(bank):
    """收集题库中所有已存在的题目ID，用于幂等检查。"""
    ids = set()
    for sub in SUBJECTS:
        for q in bank[sub]['exams']:
            ids.add(q['id'])
    return ids


def update_meta(bank):
    """更新题库的统计元信息：各学科 total、总题数、覆盖年份。"""
    for sub in SUBJECTS:
        bank[sub]['total'] = len(bank[sub]['exams'])
    total = sum(bank[sub]['total'] for sub in SUBJECTS)
    bank['meta']['totalQuestions'] = total
    all_years = set()
    for sub in SUBJECTS:
        for q in bank[sub]['exams']:
            all_years.add(q['year'])
    bank['meta']['years'] = sorted(list(all_years))
    bank['meta']['description'] = f"广东省高考真题库 ({len(all_years)}年完整覆盖)"


def make_id(prefix, year, num, width=2):
    """生成题目ID：{前缀}_{年份}_{补零编号}。"""
    return f"{prefix}_{year}_{str(num).zfill(width)}"


def build_analysis(question, options, ans_idx, category, hint=None):
    """
    构建改进版解析：包含具体答案内容与知识点原理说明，避免套路化模板。
    若提供 hint（手工解析），则直接使用 hint。
    """
    if hint:
        return hint
    answer_letter = chr(65 + ans_idx)
    answer_text = options[ans_idx] if ans_idx < len(options) else ""
    q_stem = question.replace('？', '').replace('?', '').strip()
    return (f"{q_stem}，正确答案为{answer_letter}（{answer_text}）。"
            f"本题考查{category}相关知识，需准确理解其基本概念与规律，"
            f"由{category}的基本原理可判断{answer_text}为正确选项。")


