    generated = generate_batch(batch_num)
    to_add = {'physics': [], 'chemistry': [], 'biology': []}
    skipped = 0
    for sub in SUBJECTS:
        for q in generated[sub]:
            if idempotent and q['id'] in existing_ids:
                skipped += 1
                continue
            to_add[sub].append(q)
            existing_ids.add(q['id'])
    return to_add, skipped


